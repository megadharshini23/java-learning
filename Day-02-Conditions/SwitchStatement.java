public class SwitchStatement {
    public static void main(String[] args) {
         int order=3;
         switch (order) {
            case 2:
                System.out.println("idly");
                break;
            case 3:
                System.out.println("dosa");
                break;
            case 4:
                System.out.println("poori");
                break;
            default:
                System.out.println("poori");
                break;
         }
    }
}
